<template>
    <div class="anim_container">
       <transition 
       v-on:before-enter="beforeEnter" 
       v-on:enter="enter"
       >
           <div class="icon icon_phone" delay="0" left="100">

           </div>
       </transition>
    </div>
</template>

<script>
export default {
    data() {
        return {

        }
    },
    methods: {
        beforeEnter(el) {
            el.style.left = '-200px';
            el.style.opacity = 0;
        },
        enter(el, done) {
            let delay = parseInt(el.getAttribute('delay'));
            let left = parseInt(el.getAttribute('left'));
    
            Velocity(el, { opacity: 1, left:left}, { delay:delay, duration: 2000, complete: done});
            
        }
    }
}
</script>

